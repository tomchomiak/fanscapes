$(function(){

  $('#carousel-clients').carousel({
    interval: 3500
  });

  $('#carousel').carousel({
    interval: 3500
  });

  $('#carousel-clients .item').each(function(){
    var next = $(this).next();
    if (!next.length) {
      next = $(this).siblings(':first');
    }
    next.children(':first-child').clone().appendTo($(this));

    for (var i=0;i<2;i++) {
      next=next.next();
      if (!next.length) {
        next = $(this).siblings(':first');
      }

      next.children(':first-child').clone().appendTo($(this));
    }
  });




})